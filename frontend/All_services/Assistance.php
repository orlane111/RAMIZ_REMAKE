<!-- Script pour l'animation de l'accordéon -->
<script>
document.addEventListener('DOMContentLoaded', function() {
    // Initialisation manuelle de l'accordéon
    const accordionButtons = document.querySelectorAll('.accordion-button');
    
    accordionButtons.forEach(button => {
        button.addEventListener('click', function() {
            const target = document.querySelector(this.getAttribute('data-bs-target'));
            
            // Ferme tous les autres panneaux
            document.querySelectorAll('.accordion-collapse').forEach(collapse => {
                if (collapse !== target) {
                    collapse.classList.remove('show');
                    
                    // Trouve le bouton correspondant et le marque comme collapsed
                    const buttonSelector = `[data-bs-target="#${collapse.id}"]`;
                    document.querySelector(buttonSelector).classList.add('collapsed');
                }
            });
            
            // Bascule l'état du panneau actuel
            this.classList.toggle('collapsed');
            target.classList.toggle('show');
        });
    });
});
</script>

